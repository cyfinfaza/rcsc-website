<h2>About</h2>
<p>
	A hackathon is an event where students individually or collaboratively create innovative solutions
	with technology, often using hardware, software, or both! Students present and share their ideas
	with other participants and can be awarded with prizes for their work. In addition, due to many
	workshops and experienced mentors, a hackathon is a great learning opportunity and experience.
	Overall, hackathons are an excellent event for any student with an interest in computer science
	and programming to attend, no matter the skill level.
</p>
<p>
	Ridgehacks is a <strong>free</strong> 12 hour hackathon running from 8AM to 8PM with judging from industry,
	professional, and academic leaders lasting until 10PM.
</p>
<p>
	We will provide breakfast, lunch, dinner, and snacks throughout the day along with generous prizes
	and swag so stay tuned for more updates!
</p>
<p>
	For any questions or sponsorship inquiries please contact:
	<a href="mailto:ridgecompsci@ridgehacks.us">ridgecompsci@ridgehacks.us</a>
</p>
